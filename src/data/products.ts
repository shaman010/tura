function normalizeDetails(details: string[]) {
  return details.map((detail) => {
    const lower = detail.toLowerCase()
    if (lower.includes('100% хлопок')) return 'легкая фактура'
    if (lower.includes('100% лен')) return 'лен-микс'
    if (lower.includes('натуральный состав')) return 'комфорт на каждый день'
    if (lower.includes('доставка по казахстану')) return 'наличие уточняет продавец'
    return detail
  })
}

