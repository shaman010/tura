// Простой ИИ-ассистент магазина: отвечает по ключевым словам.
// Это не LLM (бэкенда нет), но ведёт себя как консультант бренда.

export function sellerGreeting(brand: string): string {
  return `Здравствуйте! Я ассистент ${brand} 👋 Помогу с размером, доставкой, оплатой и наличием. Что вас интересует?`
}

export const QUICK_REPLIES = ['Как получить?', 'Как подобрать размер?', 'Условия', 'Как оставить заявку', 'Есть в наличии?']

export function botReply(text: string, brand: string): string {
  const t = text.toLowerCase()
  const has = (...kw: string[]) => kw.some((k) => t.includes(k))

  if (has('привет', 'здрав', 'добр')) return sellerGreeting(brand)
  if (has('достав', 'когда придёт', 'сколько ждать', 'курьер'))
    return `Наличие, оплату и доставку ${brand} подтвердит после вашей заявки.`
  if (has('возврат', 'обмен', 'не подош', 'вернуть'))
    return `Возврат и обмен — 30 дней с момента получения, без объяснения причин. Вещь должна сохранить бирки. Оформляется в пару кликов.`
  if (has('размер', 'подобрать', 'маломер', 'большемер', 'таблица'))
    return `Подскажу с размером 📏 Укажите рост и обычный размер — или заполните «Мои размеры» в профиле, и мы порекомендуем автоматически. Большинство моделей ${brand} в размер.`
  if (has('оплат', 'картой', 'kaspi', 'налич', 'рассроч'))
    return `Оставьте заявку — продавец свяжется с вами и подтвердит оплату, наличие и доставку.`
  if (has('налич', 'есть ли', 'в наличии', 'остал', 'склад'))
    return `Большинство позиций в наличии и готовы к отправке сегодня. Откройте товар — там указаны доступные размеры и цвета.`
  if (has('цена', 'скидк', 'сейл', 'sale', 'акци', 'дешевле'))
    return `Актуальные цены и скидки — прямо в карточках. Загляните в раздел Sale: там лучшие предложения сезона.`
  if (has('материал', 'состав', 'ткань', 'из чего'))
    return `Состав указан в карточке товара в блоке «Детали». В коллекции много натуральных тканей — лён, хлопок, шёлк.`
  if (has('спасибо', 'благодар', 'понял'))
    return `Всегда рады помочь! Хорошего шопинга в ${brand} 💫`
  return `Спасибо за вопрос! По нему лучше уточнить детали в карточке товара. Могу помочь с доставкой, размером, возвратом или оплатой — выберите тему ниже.`
}

